function getCurrentLanguage() {
    return localStorage.getItem('language') || 'zh';
}

function setLanguage(lang) {
    localStorage.setItem('language', lang);
}

function toggleLanguage() {
    const currentLang = getCurrentLanguage();
    const newLang = currentLang === 'zh' ? 'en' : 'zh';
    setLanguage(newLang);
    updatePageContent(newLang);
}

function updatePageContent(lang) {
    const pageName = document.body.getAttribute('data-page');
    if (!pageName || !translations[pageName]) return;
    
    const trans = translations[pageName][lang];
    
    const langBtn = document.getElementById('langToggle');
    if (langBtn) {
        langBtn.textContent = trans.langBtn;
    }
    
    if (pageName === 'index') {
        updateIndexPage(trans);
    } else if (pageName === 'about') {
        updateAboutPage(trans);
    } else if (pageName === 'screenwriting') {
        updateScreenwritingPage(trans);
    } else if (pageName === 'video') {
        updateVideoPage(trans);
    }
}
function updateVideoPage(trans) {
    const title = document.querySelector('header h1');
    if (title) title.textContent = trans.title;
}
function updateIndexPage(trans) {
    const title = document.querySelector('header h1');
    if (title) title.textContent = trans.title;
    
    const labels = document.querySelectorAll('.label');
    if (labels[0]) labels[0].textContent = trans.about;
    if (labels[1]) labels[1].textContent = trans.myFilming;
    if (labels[2]) labels[2].textContent = trans.showreel;
    
    const footerP = document.querySelector('footer p');
    if (footerP) footerP.textContent = trans.contactTitle;
    
    const footerSpan = document.querySelector('footer span');
    if (footerSpan) footerSpan.textContent = trans.contactDesc;
}

function updateAboutPage(trans) {
    const title = document.querySelector('header h1');
    if (title) title.textContent = trans.title;
    
    const subtitle = document.querySelector('.subtitle');
    if (subtitle) subtitle.textContent = trans.subtitle;
    
    const backLink = document.querySelector('.back-link');
    if (backLink) backLink.textContent = trans.back;
    
    const aboutContactP = document.querySelector('.about-contact p');
    if (aboutContactP) aboutContactP.textContent = trans.school;
    
    const sectionTitles = document.querySelectorAll('.section-title');
    if (sectionTitles[0]) sectionTitles[0].textContent = trans.aboutMeTitle;
    if (sectionTitles[1]) sectionTitles[1].textContent = trans.skillsTitle;
    if (sectionTitles[2]) sectionTitles[2].textContent = trans.moreTitle;
    if (sectionTitles[3]) sectionTitles[3].textContent = trans.contactTitle;
    
    const bioText = document.querySelector('.bio-text p');
    if (bioText) bioText.textContent = trans.aboutMeText;
    
    const skillsList = document.querySelectorAll('.skills-list');
    if (skillsList[0]) skillsList[0].textContent = trans.skills1;
    if (skillsList[1]) skillsList[1].textContent = trans.skills2;
    if (skillsList[2]) skillsList[2].textContent = trans.moreText;
    
    const contactSectionP = document.querySelector('.contact-section p');
    if (contactSectionP) contactSectionP.textContent = trans.contactDesc;
}

function updateScreenwritingPage(trans) {
    const title = document.querySelector('header h1');
    if (title) title.textContent = trans.title;
    
    const subtitle = document.querySelector('.subtitle');
    if (subtitle) subtitle.textContent = trans.subtitle;
    
    const backLink = document.querySelector('.back-link');
    if (backLink) backLink.textContent = trans.back;
    
    const swItems = document.querySelectorAll('.sw-item');
    swItems.forEach((item, index) => {
        if (trans.projects[index]) {
            const title = item.querySelector('.sw-title');
            const role = item.querySelector('.sw-synopsis-label');
            const body = item.querySelector('.sw-body');
            
            if (title) title.textContent = trans.projects[index].title;
            if (role) role.textContent = trans.projects[index].role;
            if (body) body.textContent = trans.projects[index].desc;
        }
    });
}

document.addEventListener('DOMContentLoaded', function() {
    const currentLang = getCurrentLanguage();
    updatePageContent(currentLang);
});

